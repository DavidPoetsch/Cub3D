#include "../includes/libft.h"

/**
 * @brief The memchr() function scans the initial n bytes of the
					memory  area pointed to by s for the first instance of c.
					Both c and the bytes of the memory area pointed to  by  s
					are interpreted as unsigned char.
 * @param s	The memory area to scan
 * @param c	The character to find
 * @param n Amount of bytes to scan
 * @return void*	returns a pointer to the matching byte or NULL if
									the character does not occur.
 */
void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*ptr;

	i = 0;
	ptr = (unsigned char *)s;
	while (i < n)
	{
		if (ptr[i] == (unsigned char)c)
			return ((void *)&ptr[i]);
		i++;
	}
	return (NULL);
}
