#include "cub3D.h"

void	print_map_lst(t_map_lst *map)
{
	t_map_lst	*curr;
	int			i;

	i = 0;
	curr = map;
	printf("--------------------------LIST--------------------------\n");
	while (curr)
	{
		printf("%3d.%s", i, curr->line);
		i++;
		curr = curr->next;
	}
	printf("\n--------------------------------------------------------\n");
}

void	print_map_arr(char **arr)
{
	int	i;

	i = 0;
	printf("--------------------------ARRAY-------------------------\n");
	while (arr[i])
	{
		printf("%3d.%s\n", i, arr[i]);
		i++;
	}
	printf("--------------------------------------------------------\n");
}
